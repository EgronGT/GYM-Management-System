    private void changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeActionPerformed
        
        DefaultTableModel model= (DefaultTableModel) jTable.getModel();
            
        
            if(jTable.getSelectedRow()==-1){
            if (jTable.getRowCount()==0){
                // qofse selectet row esht bosh tabela nuk shfaq as nje gje
                JOptionPane.showMessageDialog(null, "Table is empty");
            }else{
                JOptionPane.showMessageDialog(null, "Please select an Item");
            }
            }else{
            model.setValueAt(Name.getText(), jTable.getSelectedRow(), 1);
            model.setValueAt(Surname.getText(), jTable.getSelectedRow(), 2);
            model.setValueAt(Height.getText(), jTable.getSelectedRow(), 3);
            model.setValueAt(Service.getSelectedItem().toString(), jTable.getSelectedRow(), 4);
            model.setValueAt(Nr.getText().toString(), jTable.getSelectedRow(), 5);
            String Gender = null;
            
            if(man.isSelected()==true)
                Gender="Male";
            else if(woman.isSelected()==true)
                Gender="Female";
            model.setValueAt(Gender, jTable.getSelectedRow(), 6);
            
            
            // kjo esht pjesa qe rimbush pjesat ne te majten me vlerat e rreshtit te selektuar ne tabel 
        
    }
        
    }//GEN-LAST:event_changeActionPerformed

