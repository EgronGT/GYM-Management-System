    public void add(){
        try{
            
            int time = 0;
          switch(Service.getSelectedIndex()){
              case 0:
                  time=1;
                  break;
              case 1:
                  time=3;
                  break;
              case 2:
                  time=6;
                  break;
              case 3:
                  time=12;
                  break;
          }
// kjo esht nje metod qe ben shtesen e vlerave nga secilla fush ne array 
          int month=(Date.getDate().getMonth()+1)+time;
          int year=(Date.getDate().getYear()+1900)+month/12;
          if (month>12)
          month=month%12;
          
            
       arrID[cur] = txtid.getText();
//       jTable.setValueAt(cur, tblRows) = arrID[cur];

        
       arrHeight[cur] = Height.getText();
       arrName[cur] = Name.getText();
       arrSurname[cur] = Surname.getText();
       arrService[cur] = Service.getSelectedItem().toString();
       arrType[cur] = Nr.getText();
       arrDate[cur] = (Date.getDate().getMonth()+1)+"-"+Date.getDate().getDate()+"-"+(Date.getDate().getYear()+1900);
       arrD[cur] = month+"-"+Date.getDate().getDate()+"-"+year;
       arrDateR[cur] = Date.getDate().toString();
       arrDateD[cur] = Date.getDate().toString();
       
       // mer textin nga fusha pare nga e dyta edhe nga tjera edhe i fut ne secillen ne array prej te pares deri te e fundit
       
       if(man.isSelected()){
       arrGender[cur] = "Male";
       }else if(woman.isSelected()){
       arrGender[cur] = "Female";
       }else
           arrGender[cur]=null;
       
    
       switch(arrService[cur]){
           case "One Month": arrPrice[cur] = "35$";
           break;
           case "Tree Months": arrPrice[cur] = "100$";
           break;
           case "Six Months": arrPrice[cur] = "180$";
           break;
           case "One Year": arrPrice[cur] = "240$";
           break;
       }
       cur++;
        }catch(Exception e){
            System.out.println("Yess");
        }
       
       // kena perdor nje kusht per selectin qe na jep nje cmin te ndryshem nga secilli emer
    }
    
